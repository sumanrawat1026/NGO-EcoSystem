    private void btnRequestToEducationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestToEducationActionPerformed
        // TODO add your handling code here:
        //EducationWorkRequest request1 = new EducationWorkRequest();
        if (txtStatus.getText().equals("APPROVED") ){
           JOptionPane.showMessageDialog(null , " Request Already Approved"); 
           return;
        }
        else if (txtStatus.getText().equals("Send To Education")) {
           JOptionPane.showMessageDialog(null , " Request Already Sent To Education"); 
           return;
        }
        else {
        for(Network network:system.getNetworkList()){
                for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
//                    System.out.println(system);
                    if (enterprise.getEnterpriseType() == Enterprise.EnterpriseType.Education){
                       enterprise.getWorkQueue().getOutsideGarmentWorkRequests().add(request);
                        //userAccount.getWorkQueue().getOutsideGarmentWorkRequests().add(request);
                        ApproveJButton.setEnabled(false);
                        request.setStatus("Send To Education");
                        populateRequestTableGarment();
                    }
        
        }       }    }  
    }//GEN-LAST:event_btnRequestToEducationActionPerformed

