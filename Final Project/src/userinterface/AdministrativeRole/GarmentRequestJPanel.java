    private void ApproveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApproveJButtonActionPerformed

//        int selectedRow = workRequestJTable.getSelectedRow();
//
//        if (selectedRow < 0){
//            return;
//        }
        if (txtStatus.getText().equals("APPROVED")) {
            JOptionPane.showMessageDialog(null , " Request Already Approved");
            btnRequestToEducation.setEnabled(false);
            ApproveJButton.setEnabled(false);
            return;
        }
        else if (txtStatus.getText().equals("Send To Education")) {
           JOptionPane.showMessageDialog(null , " Request Already Sent To Education"); 
           return;
        }
        
        else{        
            request.setStatus("APPROVED");
            request.setRealReciver(enterprise.getName());
            populateRequestTableGarment();
            JOptionPane.showMessageDialog(null, "Request Successfully Approved");
        }
    }//GEN-LAST:event_ApproveJButtonActionPerformed

